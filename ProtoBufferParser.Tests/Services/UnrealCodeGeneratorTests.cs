using ProtoBufferParser.Models;
using ProtoBufferParser.Services;
using ProtoBufferParser.Tests.Helpers;

namespace ProtoBufferParser.Tests.Services;

public sealed class UnrealCodeGeneratorTests
{
    private readonly UnrealTypeMapper _typeMapper = new();
    private readonly MockLogger _logger = new();

    private UnrealCodeGenerator CreateGenerator() =>
        new(_typeMapper, _logger);

    private static ProtoFileNode CreateSimpleProtoFile()
    {
        return new ProtoFileNode
        {
            FileName = "player.proto",
            Package = "game",
            Imports = new List<ImportNode>(),
            Messages = new List<MessageNode>
            {
                new()
                {
                    Name = "Player",
                    FullName = "Player",
                    ProtocTypeName = "Player",
                    Fields = new List<FieldNode>
                    {
                        new() { Name = "id", Type = "int32", FieldNumber = 1 },
                        new() { Name = "name", Type = "string", FieldNumber = 2 }
                    }
                }
            },
            Enums = new List<EnumNode>()
        };
    }

    // ========== Basic Generation ==========

    [Fact]
    public void Generate_SimpleMessage_ReturnsOneType()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);

        Assert.Single(result.Types);
        Assert.Equal("FPlayerProto", result.Types[0].TypeName);
    }

    [Fact]
    public void Generate_SimpleMessage_GeneratesHeaderFile()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);

        Assert.Equal("MEPlayerProto.h", result.Types[0].HeaderFile.FileName);
        Assert.Contains("struct FPlayerProto", result.Types[0].HeaderFile.Content);
    }

    [Fact]
    public void Generate_SimpleMessage_GeneratesCppFile()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);

        Assert.NotNull(result.Types[0].CppFile);
        Assert.Equal("MEPlayerProto.cpp", result.Types[0].CppFile!.FileName);
        Assert.Contains("FPlayerProto::FPlayerProto(const game::Player& proto)", result.Types[0].CppFile!.Content);
    }

    [Fact]
    public void Generate_SimpleMessage_HeaderContainsAutoGeneratedComment()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);

        Assert.Contains("AUTO-GENERATED from player.proto", result.Types[0].HeaderFile.Content);
    }

    [Fact]
    public void Generate_SimpleMessage_CppContainsAutoGeneratedComment()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);

        Assert.Contains("AUTO-GENERATED from player.proto", result.Types[0].CppFile!.Content);
    }

    // ========== Enum Generation ==========

    [Fact]
    public void Generate_EnumOnly_ReturnsEnumType()
    {
        var generator = CreateGenerator();
        var ast = new ProtoFileNode
        {
            FileName = "status.proto",
            Imports = new List<ImportNode>(),
            Messages = new List<MessageNode>(),
            Enums = new List<EnumNode>
