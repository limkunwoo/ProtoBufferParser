    // ========== Mixed Messages and Enums ==========

    [Fact]
    public void Generate_MixedMessagesAndEnums_ReturnsAll()
    {
        var generator = CreateGenerator();
        var ast = new ProtoFileNode
        {
            FileName = "game.proto",
            Imports = new List<ImportNode>(),
            Messages = new List<MessageNode>
            {
                new()
                {
                    Name = "Player",
                    FullName = "Player",
                    ProtocTypeName = "Player",
                    Fields = new List<FieldNode>
                    {
                        new() { Name = "id", Type = "int32", FieldNumber = 1 }
                    }
                },
                new()
                {
                    Name = "Item",
                    FullName = "Item",
                    ProtocTypeName = "Item",
                    Fields = new List<FieldNode>
                    {
                        new() { Name = "name", Type = "string", FieldNumber = 1 }
                    }
                }
            },
            Enums = new List<EnumNode>
            {
                new()
                {
                    Name = "Status",
                    FullName = "Status",
                    Values = new List<EnumValueNode>
                    {
                        new() { Name = "UNKNOWN", Value = 0 }
                    }
                }
            }
        };

        var result = generator.Generate(ast);

        // Enums come first, then messages
        Assert.Equal(3, result.Types.Count);
        Assert.Equal("EStatusProto", result.Types[0].TypeName);
        Assert.Equal("FPlayerProto", result.Types[1].TypeName);
        Assert.Equal("FItemProto", result.Types[2].TypeName);
    }

    // ========== AllFiles Property ==========

    [Fact]
    public void Generate_AllFiles_FlattensProperly()
    {
        var generator = CreateGenerator();
        var ast = new ProtoFileNode
        {
            FileName = "game.proto",
            Imports = new List<ImportNode>(),
            Messages = new List<MessageNode>
            {
                new()
                {
                    Name = "Player",
                    FullName = "Player",
                    ProtocTypeName = "Player",
                    Fields = new List<FieldNode>
                    {
                        new() { Name = "id", Type = "int32", FieldNumber = 1 }
                    }
                }
            },
            Enums = new List<EnumNode>
            {
                new()
                {
                    Name = "Status",
                    FullName = "Status",
                    Values = new List<EnumValueNode>
                    {
                        new() { Name = "UNKNOWN", Value = 0 }
                    }
                }
            }
        };

        var result = generator.Generate(ast);
        var allFiles = result.AllFiles.ToList();

        // Enum: 1 header only (no cpp), Message: 1 header + 1 cpp = 3 total
        Assert.Equal(3, allFiles.Count);
        Assert.Contains(allFiles, f => f.FileName == "MEStatusProto.h");
        Assert.Contains(allFiles, f => f.FileName == "MEPlayerProto.h");
        Assert.Contains(allFiles, f => f.FileName == "MEPlayerProto.cpp");
    }

    // ========== Dependencies ==========

    [Fact]
    public void Generate_MessageWithImports_NoFieldUsage_NoImportIncludes()
    {
        var generator = CreateGenerator();
        var ast = new ProtoFileNode
        {
            FileName = "game.proto",
            Imports = new List<ImportNode>
            {
                new() { Path = "common.proto" }
            },
            Messages = new List<MessageNode>
            {
                new()
                {
                    Name = "Player",
                    FullName = "Player",
                    ProtocTypeName = "Player",
                    Fields = new List<FieldNode>
                    {
                        new() { Name = "id", Type = "int32", FieldNumber = 1 }
                    }
                }
            },
            Enums = new List<EnumNode>()
        };

        var result = generator.Generate(ast);

        // Import exists but Player doesn't use any type from common.proto,
        // so no import-based include should be generated.
        Assert.DoesNotContain("#include \"MECommonProto.h\"", result.Types[0].HeaderFile.Content);
    }

