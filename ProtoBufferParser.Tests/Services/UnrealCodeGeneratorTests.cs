    // ========== End-to-End: Full header content structure ==========

    [Fact]
    public void Generate_VerifyFullHeaderStructure()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);
        var header = result.Types[0].HeaderFile.Content;

        // Verify order: comment → pragma → includes → USTRUCT → struct → fields
        var commentIdx = header.IndexOf("AUTO-GENERATED");
        var pragmaIdx = header.IndexOf("#pragma once");
        var coreMinimalIdx = header.IndexOf("#include \"CoreMinimal.h\"");
        var generatedIdx = header.IndexOf(".generated.h\"");
        var ustructIdx = header.IndexOf("USTRUCT(BlueprintType)");
        var structIdx = header.IndexOf("struct FPlayerProto");

        Assert.True(commentIdx < pragmaIdx, "Comment should come before #pragma once");
        Assert.True(pragmaIdx < coreMinimalIdx, "#pragma once should come before CoreMinimal");
        Assert.True(coreMinimalIdx < generatedIdx, "CoreMinimal should come before .generated.h");
        Assert.True(generatedIdx < ustructIdx, ".generated.h should come before USTRUCT");
        Assert.True(ustructIdx < structIdx, "USTRUCT should come before struct definition");
    }

    // ========== End-to-End: Full cpp content structure ==========

    [Fact]
    public void Generate_VerifyFullCppStructure()
    {
        var generator = CreateGenerator();
        var ast = CreateSimpleProtoFile();

        var result = generator.Generate(ast);
        var cpp = result.Types[0].CppFile!.Content;

        // Verify order: comment → self-include → pb.h include → constructor
        var commentIdx = cpp.IndexOf("AUTO-GENERATED");
        var selfIncludeIdx = cpp.IndexOf("#include \"MEPlayerProto.h\"");
        var pbIncludeIdx = cpp.IndexOf("#include \"player.pb.h\"");
        var constructorIdx = cpp.IndexOf("FPlayerProto::FPlayerProto");

        Assert.True(commentIdx < selfIncludeIdx, "Comment should come before self-include");
        Assert.True(selfIncludeIdx < pbIncludeIdx, "Self-include should come before .pb.h include");
        Assert.True(pbIncludeIdx < constructorIdx, ".pb.h include should come before constructor");
    }
