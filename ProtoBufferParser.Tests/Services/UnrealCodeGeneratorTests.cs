        var result = generator.Generate(ast);

        Assert.Single(result.Types);
        Assert.Equal("EStatusProto", result.Types[0].TypeName);
        Assert.Equal("MEStatusProto.h", result.Types[0].HeaderFile.FileName);
        Assert.Contains("enum class EStatusProto : uint8", result.Types[0].HeaderFile.Content);
    }

